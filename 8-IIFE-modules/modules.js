// ¿Por qué usar módulos?
// Antes de ES6, JavaScript no tenía un sistema de módulos nativo, lo que dificultaba estructurar aplicaciones grandes. Los módulos ayudan a:

// Mantener el código limpio y organizado: Separando la lógica en piezas reutilizables.
// Evitar la contaminación del ámbito global: Cada módulo tiene su propio espacio de nombres.
// Facilitar la reutilización: Puedes usar el mismo código en diferentes partes de una aplicación o incluso en otros proyectos.
// Mejorar el mantenimiento: Es más fácil localizar y corregir errores o hacer cambios.
